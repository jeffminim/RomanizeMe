export default function RusCyrillic(text: string): string {
  // 西里尔字母到拉丁字母的映射表（ISO 9标准）
  const cyrillicToLatinMap: Record<string, string> = {
    'А': 'A', 'а': 'a',
    'Б': 'B', 'б': 'b',
    'В': 'V', 'в': 'v',
    'Г': 'G', 'г': 'g',
    'Д': 'D', 'д': 'd',
    'Е': 'E', 'е': 'e',
    'Ё': 'Ë', 'ё': 'ë',
    'Ж': 'Ž', 'ж': 'ž',
    'З': 'Z', 'з': 'z',
    'И': 'I', 'и': 'i',
    'Й': 'J', 'й': 'j',
    'К': 'K', 'к': 'k',
    'Л': 'L', 'л': 'l',
    'М': 'M', 'м': 'm',
    'Н': 'N', 'н': 'n',
    'О': 'O', 'о': 'o',
    'П': 'P', 'п': 'p',
    'Р': 'R', 'р': 'r',
    'С': 'S', 'с': 's',
    'Т': 'T', 'т': 't',
    'У': 'U', 'у': 'u',
    'Ф': 'F', 'ф': 'f',
    'Х': 'H', 'х': 'h',
    'Ц': 'C', 'ц': 'c',
    'Ч': 'Č', 'ч': 'č',
    'Ш': 'Š', 'ш': 'š',
    'Щ': 'Ŝ', 'щ': 'ŝ',
    'Ъ': '"', 'ъ': '"',
    'Ы': 'Y', 'ы': 'y',
    'Ь': "'", 'ь': "'",
    'Э': 'È', 'э': 'è',
    'Ю': 'Û', 'ю': 'û',
    'Я': 'Â', 'я': 'â'
  };

  let result = '';
  
  // 逐个字符转换
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    // 如果字符在映射表中，则转换，否则保留原字符
    result += cyrillicToLatinMap[char] || char;
  }

  return result;
} 